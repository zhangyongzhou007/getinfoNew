	public static List<OrgInfoEntity> parserToOrgs(
			List<Map<String, String>> result)
	{
		List<OrgInfoEntity> infos = new ArrayList<OrgInfoEntity>();
		for (Map<String, String> item : result)
		{
			OrgInfoEntity org = new OrgInfoEntity();
			org.setOrgId(item.get("org-id"));
			org.setName(item.get("name"));
			org.setAgreeType(item.get("agree-type"));
			org.setKeyCode(item.get("key-code"));
			infos.add(org);
		}
		return infos;
	}
	
	
